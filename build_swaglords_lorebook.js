// ═══ REGEX HELPERS ═══
function esc(s) { return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'); }
function en(w, cs) { return `/${cs?'':''}\\b${esc(w).replace(/\s+/g,'\\s+')}\\b/${cs?'':'i'}`; }
function ruS(w) {
  if (w.includes(' ')) return `/(?:^|[^а-яёА-ЯЁ])${esc(w).replace(/\s+/g,'\\s+')}(?=[^а-яёА-ЯЁ]|$)/i`;
  let s = w;
  if (/(?:ый|ий|ой|ая|яя|ое|ее|ые|ие)$/.test(s)&&s.length>4) s=s.slice(0,-2);
  else if (/(?:ость|есть)$/.test(s)&&s.length>5) s=s.slice(0,-1);
  else if (/(?:ние|тие|ция|зия)$/.test(s)&&s.length>5) s=s.slice(0,-1);
  else if (/[ая]$/.test(s)&&s.length>3) s=s.slice(0,-1);
  else if (/ь$/.test(s)&&s.length>3) s=s.slice(0,-1);
  else if (/[ыи]$/.test(s)&&s.length>4) s=s.slice(0,-1);
  else if (/[ое]$/.test(s)&&s.length>3) s=s.slice(0,-1);
  if (s.length<3) return `/(?:^|[^а-яёА-ЯЁ])${esc(w)}(?=[^а-яёА-ЯЁ]|$)/i`;
  return `/(?:^|[^а-яёА-ЯЁ])${esc(s)}[а-яёА-ЯЁ]*(?=[^а-яёА-ЯЁ]|$)/i`;
}
function ruE(w) { return `/(?:^|[^а-яёА-ЯЁ])${esc(w)}(?=[^а-яёА-ЯЁ]|$)/i`; }

function K(...args) { return args.map(k => {
  if (k.startsWith('/')) return k; // already regex
  if (/[а-яёА-ЯЁ]/.test(k)) return k.length <= 3 ? ruE(k) : ruS(k);
  return en(k);
}); }

let uid = 0;
function E(comment, keys, content, opts = {}) {
  const id = uid++;
  return {
    uid: id,
    key: K(...keys),
    keysecondary: [],
    comment,
    content,
    constant: opts.constant || false,
    selective: false,
    selectiveLogic: 0,
    addMemo: true,
    order: opts.order || 100,
    position: opts.position !== undefined ? opts.position : 0,
    disable: false,
    probability: 100,
    useProbability: true,
    depth: 4,
    sticky: opts.sticky || 0,
    vectorized: false,
    ignoreBudget: false,
    excludeRecursion: false,
    preventRecursion: opts.preventRecursion !== undefined ? opts.preventRecursion : true,
    displayIndex: id,
  };
}

